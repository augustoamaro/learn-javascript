// Push - adiciona no fim do array
// Pop - Remove do fim do array
// Shift - Remove do começo do array
// Unshift - adiciona no começo do array

let topSongs = [
    'Going The Distance',
    'Hustlers Ambition',
    'Live For The Kill',
    'Rich Nigga Problems'
];

topSongs.push('Living For The Moment')
console.log(topSongs)
topSongs.pop()
console.log(topSongs)
topSongs.shift()
console.log(topSongs)
topSongs.unshift('One')
console.log(topSongs)