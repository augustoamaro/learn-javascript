// Operadores unários é uma operação, composta por dois simbolos, que não precisam estar necessariamente entre dois termos.

let score = 0;
score = score + 10;
score += 1; // Mesma coisa que score = score + 1
console.log(score)

score++; // Incrementa o valor
console.log(score)

score--; // Decrementa o valor
console.log(score)